export const checkIfUsernameIsAllowed = (username: string) => {
  // Check username length
  if (username.length < 2 || username.length > 30) return false;

  // Check if username starts with a period
  if (username.startsWith(".")) return false;

  // Check if username is only numbers
  if (!isNaN(Number(username))) return false;

  // Check if username is in banned words list
  if (DISALLOWED_USERNAMES.includes(username)) return false;

  // Check username for invalid characters
  const regex = /^[a-zA-Z0-9._]+$/;
  return regex.test(username);
};
